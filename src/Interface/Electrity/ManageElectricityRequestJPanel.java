    private void populateTable()
    {
        
        DefaultTableModel dtm = (DefaultTableModel) receReqJTbl.getModel();
        dtm.setRowCount(0);
        
        for (Iterator<WorkRequest> it = electricityOrganisation.getWorkQueue().getReqList().iterator(); it.hasNext();) {
          //  UserAccount rec = wr.getReceiver();
             ElectricityWorkRequest wr = (ElectricityWorkRequest) it.next();
             {
                Object Row[] = new Object[5];
                Row[0] = wr.getCust();
                Row[1] = wr.getRequestDate();
                Row[2] = wr.getMessage();
                Row[3] = wr;
                Row[4] = wr.getReqCharges();

                dtm.addRow(Row);
            }
        }
    }
    
