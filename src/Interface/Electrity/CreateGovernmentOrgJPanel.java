    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        for (GovernmentType type : Organisation.GovernmentType.values()){
            //if (type.getValue().equals(MarketType.MarketAdmin.getValue()))
                organizationJComboBox.addItem(type);
        }
    }
    
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organisation organization : enterprise.getOrganisationDirectory().getOrganizationlist()){
            Object[] row = new Object[1];
            row[0] = organization.getOrgName();
            
            model.addRow(row);
        }
    }

